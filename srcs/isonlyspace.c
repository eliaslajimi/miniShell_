#include "minishell.h"

int				isonlyspace(char *s)
{
	int			i;

	i = 0;
	while (s[i] && ft_isin(s[i], " ;\t\v\f\r"))
		i++;
	if (i == ft_strlen(s))
		return (0);
	return (1);
}
